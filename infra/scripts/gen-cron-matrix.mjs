#!/usr/bin/env node
/**
 * gen-cron-matrix.mjs — 크론 × 모델 × 채널 × 일정 통합 매트릭스 생성
 *
 * 주인님 지시 (2026-05-08): "조회 질문에 매번 코드 grep하지 말고 단일 문서로 즉답"
 *
 * 출력:
 *   - infra/docs/CRON-MATRIX.md          (사람·LLM 모두 읽기 좋은 표 + 그룹별 bullet)
 *   - infra/docs/cron-matrix.json        (프로그램 read 용 구조화 데이터)
 *
 * 입력:
 *   - runtime/config/tasks.json          (cron 정의 SSoT)
 *
 * 사용:
 *   node ~/jarvis/infra/scripts/gen-cron-matrix.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '../..');
const TASKS_JSON = path.join(ROOT, 'runtime/config/tasks.json');
const OUT_MD = path.join(ROOT, 'infra/docs/CRON-MATRIX.md');
const OUT_JSON = path.join(ROOT, 'infra/docs/cron-matrix.json');

const tasks = JSON.parse(fs.readFileSync(TASKS_JSON, 'utf-8')).tasks;
const now = new Date().toISOString();

// 그룹화: schedule 빈도별 (daily/weekly/hourly/...)
const groups = { daily: [], hourly: [], weekly: [], monthly: [], adhoc: [], other: [] };
const modelCounts = {};
const channelCounts = {};

for (const t of tasks) {
  const sched = t.schedule || '';
  const model = t.model || '<default>';
  const channel = t.discordChannel || '<none>';

  modelCounts[model] = (modelCounts[model] || 0) + 1;
  channelCounts[channel] = (channelCounts[channel] || 0) + 1;

  let group = 'other';
  if (!sched) group = 'adhoc';
  else if (/^\d+ \* \* \* \*$/.test(sched)) group = 'hourly';
  else if (/^\d+ \d+ \* \* \*$/.test(sched)) group = 'daily';
  else if (/^\d+ \d+ \* \* [0-6]$/.test(sched)) group = 'weekly';
  else if (/^\d+ \d+ [0-9*]+ \* \*$/.test(sched) && !sched.includes(',')) group = 'monthly';

  groups[group].push({
    id: t.id,
    name: t.name || t.id,
    schedule: sched || '(adhoc)',
    model,
    channel,
    timeout: t.timeout || null,
    maxBudget: t.maxBudget || null,
    allowedTools: t.allowedTools || null,
  });
}

for (const k of Object.keys(groups)) {
  groups[k].sort((a, b) => (a.schedule || '').localeCompare(b.schedule || '') || a.id.localeCompare(b.id));
}

// JSON 출력
const payload = {
  generatedAt: now,
  totalTasks: tasks.length,
  source: 'runtime/config/tasks.json',
  modelDistribution: modelCounts,
  channelDistribution: channelCounts,
  groups,
};
fs.writeFileSync(OUT_JSON, JSON.stringify(payload, null, 2), 'utf-8');

// MD 출력
const md = [];
md.push('# 🗓️ Cron Matrix — 크론 × 모델 × 채널 × 일정');
md.push('');
md.push(`> 🤖 Auto-generated by \`infra/scripts/gen-cron-matrix.mjs\` — do not edit.`);
md.push(`> Generated: ${now}`);
md.push(`> Source: \`runtime/config/tasks.json\` (총 ${tasks.length} task)`);
md.push('');
md.push('## 📊 분포');
md.push('');
md.push('### 모델 분포');
for (const [m, c] of Object.entries(modelCounts).sort((a, b) => b[1] - a[1])) {
  md.push(`- \`${m}\`: ${c}건`);
}
md.push('');
md.push('### Discord 채널 분포');
for (const [ch, c] of Object.entries(channelCounts).sort((a, b) => b[1] - a[1])) {
  md.push(`- \`${ch}\`: ${c}건`);
}
md.push('');

const groupTitle = {
  hourly: '⏱️ 매시간 (Hourly)',
  daily: '🌅 매일 (Daily)',
  weekly: '📅 매주 (Weekly)',
  monthly: '🗓️ 매월 (Monthly)',
  adhoc: '🤲 수동 호출 (Ad-hoc)',
  other: '🔀 기타 패턴',
};

for (const [g, items] of Object.entries(groups)) {
  if (items.length === 0) continue;
  md.push(`## ${groupTitle[g]} — ${items.length}건`);
  md.push('');
  for (const t of items) {
    md.push(`### \`${t.id}\``);
    md.push(`- **Name**: ${t.name}`);
    md.push(`- **Schedule**: \`${t.schedule}\``);
    md.push(`- **Model**: \`${t.model}\``);
    md.push(`- **Channel**: \`${t.channel}\``);
    if (t.timeout) md.push(`- **Timeout**: ${t.timeout}s`);
    if (t.maxBudget) md.push(`- **MaxBudget**: $${t.maxBudget}`);
    if (t.allowedTools) md.push(`- **AllowedTools**: \`${t.allowedTools}\``);
    md.push('');
  }
}

fs.writeFileSync(OUT_MD, md.join('\n'), 'utf-8');

console.log(`✅ ${OUT_MD}`);
console.log(`✅ ${OUT_JSON}`);
console.log(`   tasks=${tasks.length}, models=${Object.keys(modelCounts).length}, channels=${Object.keys(channelCounts).length}`);

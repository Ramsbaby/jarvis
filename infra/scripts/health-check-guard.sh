#!/usr/bin/env bash
# health-check-guard.sh
# 클러스터 cl-bfdab567822b916f 가드: 신체 제약 정보 미확인 상태에서 일정/여행/운동 계획 제시 방지
#
# 목적:
#   일정 추천, 여행 계획, 운동 제안 요청 시 신체 제약·건강 상태를 필수 체크리스트로 확인하고,
#   필수 항목 미답변 상태에서 구체적 추천 응답을 블로킹한다.
#
# 사용법:
#   ./health-check-guard.sh <입력텍스트>       # 문자열 입력
#   echo "텍스트" | ./health-check-guard.sh    # stdin 입력
#   ./health-check-guard.sh <파일경로>         # 파일 입력
#
# 반환값:
#   0 — 검사 통과 (계획/추천 요청 아님, 또는 필수 정보 확인됨)
#   1 — 검사 불통과 (계획/추천 필요 + 신체 제약 미답변)
#   2 — 사용법 오류

set -euo pipefail

# ───────────────────────────────────────────────────────────────────────────
# [Phase 1] 일정/여행/운동 계획 요청 감지 패턴
# ───────────────────────────────────────────────────────────────────────────
# 정규식 패턴 (OR 조합): 이 중 하나라도 매칭되면 "계획/추천 요청"으로 판정
PLAN_REQUEST_PATTERNS=(
  # 일정 관련
  "일정.*추천|추천.*일정"           # "일정 추천", "일정을 추천해"
  "일정.*짜|짜.*일정"               # "일정을 짜달라", "일정 짜기"
  "일정.*정|정.*일정"               # "일정을 정해달라", "일정 정하기"
  "스케줄.*정해|정해.*스케줄"       # "스케줄 정해달라"
  "계획.*세우|세우.*계획"           # "계획을 세워달라"
  "일정.*정하|정하.*일정"           # "일정을 정해달라"

  # 여행 관련
  "여행.*계획|계획.*여행"           # "여행 계획", "여행 계획을 세워"
  "여행.*추천|추천.*여행"           # "여행을 추천해", "여행 추천해달라"
  "여행.*일정|일정.*여행"           # "여행 일정"
  "여행.*준비|준비.*여행"           # "여행 준비"
  "가볼만한|명소.*추천|관광지"      # "가볼 만한 곳", "명소 추천", "관광지 추천"
  "해외여행|국내여행"               # "해외여행 계획", "국내여행"
  "여행.*가다|가다.*여행"           # "여행 가기"

  # 운동/운동 계획 관련
  "운동.*계획|계획.*운동"           # "운동 계획", "운동 계획을 짜"
  "운동.*루틴|루틴.*운동"           # "운동 루틴", "헬스 루틴"
  "헬스.*추천|추천.*헬스"           # "헬스 추천", "헬스 프로그램 추천"
  "헬스.*프로그램|프로그램.*헬스"   # "헬스 프로그램"
  "운동.*추천|추천.*운동"           # "운동 추천", "운동 종목 추천"
  "근력.*트레이닝|트레이닝.*근력"   # "근력 트레이닝"
  "피트니스|GYM|지|짐"              # "피트니스", "GYM", "지(=gym 발음)"
  "요가.*배우|배우.*요가"           # "요가 배우기"
  "필라테스"                        # "필라테스"
  "운동.*시작|시작.*운동"           # "운동 시작하기", "운동을 시작"
  "훈련.*계획|계획.*훈련"           # "훈련 계획"
  "마라톤|조깅|등산"                # "마라톤 준비", "조깅", "등산"
)

# ───────────────────────────────────────────────────────────────────────────
# [Phase 2] 신체 제약/건강 상태 확인 패턴
# ───────────────────────────────────────────────────────────────────────────
# 아래 중 하나라도 있으면 "신체 제약 정보 확인"으로 간주
HEALTH_CHECK_PATTERNS=(
  # 일반 건강 상태
  "건강상태|건강.*상태|상태.*건강"
  "만성질환|고혈압|당뇨|심장|천식|협심증"
  "허리|무릎|발목|어깨|목|척추|디스크"
  "부상|통증|ㅠ|아픔|아프다"  # ㅠ: 고통의 의성어
  "알레르기|알러지"
  "약복용|복용.*약|약.*복용"
  "의사.*말|의료"
  "쓸수있는|할수있는"

  # 구체적 제약사항
  "금지.*사항|금지"
  "주의|조심"
  "제약|제한|한계|못"
  "가능|불가능|안됨|안된"
  "괜찮|괜찮은"
  "문제.*없|없.*문제"

  # 체력/나이 관련
  "체력|나이|몸무게|체중|신장|키"
  "기력|지구력|근력"
  "임신|산후|회복"
  "폐경|월경"
)

# ───────────────────────────────────────────────────────────────────────────
# 도움말
# ───────────────────────────────────────────────────────────────────────────
usage() {
  cat >&2 << 'EOF'
사용법: health-check-guard.sh [입력]

  입력 방식:
    ./health-check-guard.sh "일정을 추천해달라"    # 직접 문자열
    echo "텍스트" | ./health-check-guard.sh        # stdin
    ./health-check-guard.sh /path/to/file.txt      # 파일

반환값:
  0 — 검사 통과
      - 계획 요청 아님 (패턴 미매칭)
      - 또는 계획 요청 + 신체 제약 정보 확인됨
  1 — 검사 불통과 (계획 요청 감지 + 신체 제약 정보 부재)
  2 — 사용법 오류

예시:
  검사 통과:
    - "내일 뭘 하지?" → 계획 요청 아님 (PASS)
    - "운동 계획을 짜달라. 나는 허리가 안 좋아." → 신체 제약 명시 (PASS)
  검사 불통과:
    - "7월에 여행을 가려고 하는데 어디 추천해?" → 신체 제약 미명시 (FAIL)
EOF
  exit 2
}

# ───────────────────────────────────────────────────────────────────────────
# [Phase 3] 입력 처리
# ───────────────────────────────────────────────────────────────────────────
INPUT_TEXT=""
SOURCE_LABEL=""

if [[ $# -eq 0 ]]; then
  # stdin 입력
  if [[ ! -t 0 ]]; then
    INPUT_TEXT="$(cat)"
    SOURCE_LABEL="(stdin)"
  else
    usage
  fi
elif [[ $# -eq 1 ]]; then
  # 인자 1개: 파일 또는 문자열
  if [[ -f "$1" ]]; then
    INPUT_TEXT="$(cat "$1")"
    SOURCE_LABEL="$1"
  else
    # 파일이 아니면 문자열로 취급
    INPUT_TEXT="$1"
    SOURCE_LABEL="(argument)"
  fi
else
  echo "[health-check-guard] ERROR: 인자는 0개 또는 1개만 허용됩니다" >&2
  usage
fi

# 입력 검증
if [[ -z "$INPUT_TEXT" ]]; then
  echo "[health-check-guard] ERROR: 빈 입력" >&2
  exit 2
fi

# ───────────────────────────────────────────────────────────────────────────
# [Phase 4] 계획/추천 요청 감지
# ───────────────────────────────────────────────────────────────────────────
PLAN_DETECTED=false
for pattern in "${PLAN_REQUEST_PATTERNS[@]}"; do
  if echo "$INPUT_TEXT" | grep -qiE "$pattern"; then
    PLAN_DETECTED=true
    echo "[health-check-guard] INFO: 계획/추천 요청 감지 (패턴: '$pattern')" >&2
    break
  fi
done

# 계획 요청이 없으면 검사 통과
if [[ "$PLAN_DETECTED" == false ]]; then
  echo "[health-check-guard] OK: ${SOURCE_LABEL} — 계획/추천 요청 패턴 미매칭" >&2
  exit 0
fi

# ───────────────────────────────────────────────────────────────────────────
# [Phase 5] 신체 제약/건강 정보 확인
# ───────────────────────────────────────────────────────────────────────────
HEALTH_INFO_FOUND=false
for pattern in "${HEALTH_CHECK_PATTERNS[@]}"; do
  if echo "$INPUT_TEXT" | grep -qiE "$pattern"; then
    HEALTH_INFO_FOUND=true
    echo "[health-check-guard] INFO: 신체 제약 정보 확인 (패턴: '$pattern')" >&2
    break
  fi
done

# ───────────────────────────────────────────────────────────────────────────
# [Phase 6] 최종 판정
# ───────────────────────────────────────────────────────────────────────────
if [[ "$HEALTH_INFO_FOUND" == true ]]; then
  echo "[health-check-guard] PASS: ${SOURCE_LABEL} — 계획 요청 + 신체 제약 정보 확인" >&2
  exit 0
else
  {
    echo "[health-check-guard] FAIL: 신체 제약 정보 미확인"
    echo "  일정/여행/운동 계획 추천 요청이 감지되었으나, 신체 제약·건강 상태 정보가 부재합니다."
    echo ""
    echo "  필수 확인 항목 (아래 중 최소 하나):"
    echo "  □ 만성질환 유무 (고혈압, 당뇨, 심장질환 등)"
    echo "  □ 신체 부상/통증 (허리, 무릎, 발목 등)"
    echo "  □ 알레르기 유무"
    echo "  □ 약물 복용 중인 내역"
    echo "  □ 의료 제약사항 또는 제한사항"
    echo "  □ 체력 수준 또는 체중/나이 정보"
    echo "  □ 기타 신체 제약 정보"
    echo ""
    echo "  개선 방법:"
    echo "  1. 위 항목 중 해당사항을 명시하고 다시 요청하세요."
    echo "  2. 예시: \"나는 무릎이 안 좋아서 계단 많은 여행은 피하고 싶은데, 7월에 어디 추천해?\""
    echo "  3. 예시: \"허리 통증이 있어서 운동 계획을 짜주세요.\""
  } >&2
  exit 1
fi

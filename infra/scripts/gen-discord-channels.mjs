#!/usr/bin/env node
/**
 * gen-discord-channels.mjs — Discord 채널별 작업 매핑 카탈로그
 *
 * 출력:
 *   - infra/docs/DISCORD-CHANNELS.md
 *   - infra/docs/discord-channels.json
 *
 * 입력:
 *   - runtime/config/tasks.json (discordChannel 필드)
 *   - runtime/config/models.json (channelOverrides — 채널별 모델 강제)
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '../..');
const TASKS = JSON.parse(fs.readFileSync(path.join(ROOT, 'runtime/config/tasks.json'), 'utf-8')).tasks;
const MODELS = JSON.parse(fs.readFileSync(path.join(ROOT, 'infra/config/models.json'), 'utf-8'));
const OUT_MD = path.join(ROOT, 'infra/docs/DISCORD-CHANNELS.md');
const OUT_JSON = path.join(ROOT, 'infra/docs/discord-channels.json');

const now = new Date().toISOString();

// 채널별 작업 매핑
const channels = {};
for (const t of TASKS) {
  const ch = t.discordChannel || '<no-channel>';
  if (!channels[ch]) channels[ch] = { taskCount: 0, tasks: [], modelOverride: MODELS.channelOverrides?.[ch] || null, effortOverride: MODELS.effortOverrides?.[ch] || null };
  channels[ch].taskCount++;
  channels[ch].tasks.push({ id: t.id, schedule: t.schedule || '(adhoc)', model: t.model || '<default>' });
}

const sorted = Object.entries(channels).sort((a, b) => b[1].taskCount - a[1].taskCount);

// JSON 출력
fs.writeFileSync(OUT_JSON, JSON.stringify({
  generatedAt: now,
  totalChannels: sorted.length,
  totalTasks: TASKS.length,
  source: 'runtime/config/tasks.json + infra/config/models.json',
  channels: Object.fromEntries(sorted),
}, null, 2), 'utf-8');

// MD 출력
const md = [];
md.push('# 💬 Discord 채널 매핑');
md.push('');
md.push(`> 🤖 Auto-generated by \`infra/scripts/gen-discord-channels.mjs\` — do not edit.`);
md.push(`> Generated: ${now}`);
md.push(`> 총 ${sorted.length}개 채널, ${TASKS.length}개 작업`);
md.push('');
for (const [ch, info] of sorted) {
  md.push(`## \`${ch}\` — ${info.taskCount}건`);
  md.push('');
  if (info.modelOverride) md.push(`- **모델 override**: \`${info.modelOverride}\``);
  if (info.effortOverride) md.push(`- **Effort override**: \`${info.effortOverride}\``);
  md.push('');
  md.push('### 보내는 작업');
  for (const t of info.tasks.sort((a, b) => a.id.localeCompare(b.id))) {
    md.push(`- \`${t.id}\` — ${t.schedule} (모델: \`${t.model}\`)`);
  }
  md.push('');
}
fs.writeFileSync(OUT_MD, md.join('\n'), 'utf-8');

console.log(`✅ ${OUT_MD}`);
console.log(`✅ ${OUT_JSON}`);
console.log(`   channels=${sorted.length}, tasks=${TASKS.length}`);

#!/usr/bin/env node
/**
 * gen-launchagent-catalog.mjs — 모든 Jarvis LaunchAgent 일정/활성 카탈로그 생성
 *
 * 출력:
 *   - infra/docs/LAUNCHAGENT-CATALOG.md
 *   - infra/docs/launchagent-catalog.json
 *
 * 입력:
 *   - ~/Library/LaunchAgents/ai.jarvis.*.plist
 *   - launchctl list (활성 상태)
 *
 * 사용:
 *   node ~/jarvis/infra/scripts/gen-launchagent-catalog.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import os from 'node:os';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '../..');
const LA_DIR = path.join(os.homedir(), 'Library/LaunchAgents');
const OUT_MD = path.join(ROOT, 'infra/docs/LAUNCHAGENT-CATALOG.md');
const OUT_JSON = path.join(ROOT, 'infra/docs/launchagent-catalog.json');

const now = new Date().toISOString();

// launchctl list 한 번만 실행
let lcOutput = '';
try { lcOutput = execSync('launchctl list', { encoding: 'utf-8' }); } catch {}
const activeMap = {};
for (const line of lcOutput.split('\n')) {
  const m = line.match(/^([\d-]+)\s+(\d+)\s+(ai\.jarvis\.\S+)/);
  if (m) activeMap[m[3]] = { pid: m[1], lastExitCode: m[2] };
}

// plist 파싱 (XML 단순 추출)
function parsePlist(file) {
  const xml = fs.readFileSync(file, 'utf-8');
  const label = (xml.match(/<key>Label<\/key>\s*<string>([^<]+)<\/string>/) || [])[1] || path.basename(file, '.plist');
  const program = (xml.match(/<key>ProgramArguments<\/key>[\s\S]*?<\/array>/) || [''])[0]
    .match(/<string>([^<]+)<\/string>/g)?.map(s => s.replace(/<\/?string>/g, '')) || [];

  // 일정 추출
  let schedule = null;
  const interval = xml.match(/<key>StartInterval<\/key>\s*<integer>(\d+)<\/integer>/);
  if (interval) schedule = `${interval[1]}s 주기`;
  const calBlock = xml.match(/<key>StartCalendarInterval<\/key>([\s\S]*?)<\/dict>/);
  if (calBlock) {
    const fields = {};
    const matches = calBlock[1].matchAll(/<key>(\w+)<\/key>\s*<integer>(\d+)<\/integer>/g);
    for (const m of matches) fields[m[1]] = parseInt(m[2], 10);
    const wd = ['일', '월', '화', '수', '목', '금', '토'];
    const parts = [];
    if (fields.Weekday !== undefined) parts.push(`매주 ${wd[fields.Weekday]}요일`);
    else if (fields.Day !== undefined) parts.push(`매월 ${fields.Day}일`);
    else parts.push('매일');
    if (fields.Hour !== undefined) parts.push(`${String(fields.Hour).padStart(2, '0')}:${String(fields.Minute || 0).padStart(2, '0')} KST`);
    schedule = parts.join(' ');
  }
  const runAtLoad = /<key>RunAtLoad<\/key>\s*<true\/>/.test(xml);

  return {
    label,
    file: path.basename(file),
    schedule: schedule || '(없음)',
    runAtLoad,
    program: program.slice(0, 2).join(' '),
    active: !!activeMap[label],
    pid: activeMap[label]?.pid || null,
    lastExitCode: activeMap[label]?.lastExitCode || null,
  };
}

const plists = fs.readdirSync(LA_DIR)
  .filter(f => f.startsWith('ai.jarvis.') && f.endsWith('.plist'))
  .map(f => parsePlist(path.join(LA_DIR, f)))
  .sort((a, b) => a.label.localeCompare(b.label));

const activeCount = plists.filter(p => p.active).length;

// JSON 출력
fs.writeFileSync(OUT_JSON, JSON.stringify({
  generatedAt: now,
  totalLaunchAgents: plists.length,
  activeCount,
  inactiveCount: plists.length - activeCount,
  source: '~/Library/LaunchAgents/ai.jarvis.*.plist + launchctl list',
  agents: plists,
}, null, 2), 'utf-8');

// MD 출력
const md = [];
md.push('# 🚀 LaunchAgent Catalog — Jarvis 자동화 일정');
md.push('');
md.push(`> 🤖 Auto-generated by \`infra/scripts/gen-launchagent-catalog.mjs\` — do not edit.`);
md.push(`> Generated: ${now}`);
md.push(`> 총 ${plists.length}개 (활성: ${activeCount}, 비활성: ${plists.length - activeCount})`);
md.push('');
md.push('## 📋 전체 LaunchAgent');
md.push('');
for (const p of plists) {
  const mark = p.active ? '✅' : '⚠️';
  md.push(`### ${mark} \`${p.label}\``);
  md.push(`- **Schedule**: ${p.schedule}`);
  md.push(`- **Program**: \`${p.program}\``);
  md.push(`- **RunAtLoad**: ${p.runAtLoad}`);
  md.push(`- **Active**: ${p.active ? `✅ (PID ${p.pid}, last exit ${p.lastExitCode})` : '❌'}`);
  md.push('');
}
fs.writeFileSync(OUT_MD, md.join('\n'), 'utf-8');

console.log(`✅ ${OUT_MD}`);
console.log(`✅ ${OUT_JSON}`);
console.log(`   total=${plists.length}, active=${activeCount}`);
